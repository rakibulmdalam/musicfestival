package de.tum.in.dbpra.model.bean;

public class Newsletter {
	private Integer id;
	private String name;
	
	public Newsletter(){}
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}	
}
